#!/usr/bin/env bash

########################################################################################################################

get_win_pid(){
  xprop -root 32x '\t$0' _NET_ACTIVE_WINDOW | cut -f 2
}

get_win_class(){
  xprop -id "$1" '\t$0' WM_CLASS | cut -f 2 | cut -d'"' -f 2
}

get_win_name(){
  xprop -id "$1" '\t$0' _NET_WM_NAME | cut -f 2 | cut -d'"' -f 2
}

########################################################################################################################

wm_pid="$(get_win_pid)"
wm_class="$(get_win_class $wm_pid)"
wm_name="$(get_win_name $wm_pid)"

########################################################################################################################
